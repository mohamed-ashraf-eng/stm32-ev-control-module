/* USER CODE BEGIN 4 */
static void UART_Print_EV_State(float accel_pct, float brake_pct)
{
  char msg[220];

  int len = snprintf(
      msg,
      sizeof(msg),
      "MODE=%s | ACCEL=%3.0f%% | BRAKE=%3.0f%% | SPD=%6.2f km/h | TRQ=%7.2f Nm | SOC=%6.2f%% | RNG=%7.2f km | PWR=%7.2f kW | TEMP=%6.2f C | REGEN=%d\r\n",
      DriveMode_ToString(ev.drive_mode),
      accel_pct,
      brake_pct,
      ev.speed_kmh,
      ev.torque_Nm,
      ev.soc_pct,
      ev.range_km,
      ev.power_kW,
      ev.motor_temp_C,
      ev.regen_active);

  if (len > 0)
  {
    HAL_UART_Transmit(&huart1, (uint8_t*)msg, (uint16_t)len, 100);
  }
}

static const char* DriveMode_ToString(DriveMode_t mode)
{
  switch (mode)
  {
    case DRIVE_ECO:
      return "ECO";

    case DRIVE_SPORT:
      return "SPORT";

    case DRIVE_NORMAL:
    default:
      return "NORMAL";
  }
}
